//Buttons for Summoner 1

function zephyr1() {
    var button = document.getElementById("zephyr1");
    if (button.value == "Yes") {
        button.value = "No";
        button.style.background = '#ff0000';
    }
    else {
        button.value = "Yes";
        button.style.background = '#00ff00';
    }

}

function hide1() {
    var summoner1Info = document.getElementById("hide1");
    var hide1Button = document.getElementById("hideButton1");
    if (summoner1Info.style.display === "none") {
        summoner1Info.style.display = "block";
        hide1Button.value = "Hide";
    }
    else {
        summoner1Info.style.display = "none";
        hide1Button.value = "Unhide";
    }
}




